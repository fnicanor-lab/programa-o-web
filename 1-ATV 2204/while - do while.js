// Estrutura de repetição: while (enquanto/antes) 
// testa condição ANTES 
// depois executar o código (comandos/instruções) 
//  dentro do laço de repetição

let j = 1;

while (j <=10) {
    console.log(j);
    j++;
}

// Estrutura de repetição: do while (do(faça)...enquanto(while)) 
// executa primeiro código  (comandos/instruções)
//testa condição DEPOIS
// OBS : executa pelo menos 1 vez mesmo que seja  FALSO pois
// o fluxo precisa passsar pelas COMANDOS pelo menos 1 vez para TESTAR CONDIÇÃO

let k = 1;

do {
    console.log(k);
    k++;
} while(k <= 10) ;


